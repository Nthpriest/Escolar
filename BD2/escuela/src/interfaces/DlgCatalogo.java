    public DlgCatalogo(java.awt.Frame parent, boolean modal, Catalogo catalogo, StringBuffer respuesta, int operacion) {
        super(parent, modal);
        initComponents();

        this.catalogo = catalogo;
        this.respuesta = respuesta;
        this.operacion = operacion;

        if (operacion == ConstantesGUI.AGREGAR) {
            this.txtId.setText(catalogo.getIdCatalogo() + "");
            this.txtId.setBackground(Color.GRAY);
            this.txtId.setEditable(false);

            this.txtMateria.setText("");
            this.txtMateria.setBackground(Color.GRAY);
            this.txtMateria.setEditable(false);

            this.txtMaestro.setText("");
            this.txtMaestro.setBackground(Color.GRAY);
            this.txtMaestro.setEditable(false);

        }
        if (operacion == ConstantesGUI.ACTUALIZAR) {
            Materia materia = catalogo.getIdMateriaFK();
            Maestro maestro = catalogo.getIdMaestroFK();
            this.txtId.setText(catalogo.getIdCatalogo() + "");
            this.txtId.setBackground(Color.GRAY);
            this.txtId.setEditable(false);

            if (materia != null) {
                this.txtMateria.setText(materia.getNombre());
                this.txtMateria.setBackground(Color.GRAY);
                this.txtMateria.setEditable(false);
            } else {
                this.txtMateria.setText("");
                this.txtMateria.setBackground(Color.GRAY);
                this.txtMateria.setEditable(false);
            }

            if (maestro != null) {
                this.txtMaestro.setText(maestro.getNombre());
                this.txtMaestro.setBackground(Color.GRAY);
                this.txtMaestro.setEditable(false);
            } else {
                this.txtMaestro.setText("");
                this.txtMaestro.setBackground(Color.GRAY);
                this.txtMaestro.setEditable(false);
            }
        }

        // en caso de que la operacion sea de desplegar  el catalogo o de eliminar
        // definimos el comportamiento iniial
        if (operacion == ConstantesGUI.DESPLEGAR || operacion == ConstantesGUI.ELIMINAR) {
            Maestro maestro = null;
            Materia materia = null;

            this.txtId.setText(catalogo.getIdCatalogo() + "");
            this.txtId.setBackground(Color.GRAY);
            this.txtId.setEditable(false);

            this.txtHora.setText(catalogo.getHora());
            this.txtHora.setBackground(Color.GRAY);
            this.txtHora.setEditable(false);

            this.txtAula.setText(catalogo.getAula());
            this.txtAula.setBackground(Color.GRAY);
            this.txtAula.setEditable(false);

            this.JcboxPeriodo.setVisible(false);
            this.jlbMensaje.setText("Actual mente Periodo; \n" + catalogo.getPerior());

            maestro = this.catalogo.getIdMaestroFK();
            materia = this.catalogo.getIdMateriaFK();

            if (maestro != null) {
                this.txtMaestro.setText(maestro.getNombre());
                this.txtMaestro.setBackground(Color.GRAY);
                this.txtMaestro.setEditable(false);
            } else {
                this.txtMaestro.setBackground(Color.GRAY);
                this.txtMaestro.setEditable(false);
            }
            
            if (materia != null) {
                this.txtMateria.setText(materia.getNombre());
                this.txtMateria.setBackground(Color.GRAY);
                this.txtMateria.setEditable(false);
            } else {
                this.txtMateria.setBackground(Color.GRAY);
                this.txtMateria.setEditable(false);
            }

        }

        this.respuesta.delete(0, respuesta.length());
        this.respuesta.append(ConstantesGUI.CANCELAR);
        this.setVisible(true);

    }

