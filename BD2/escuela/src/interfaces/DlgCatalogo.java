    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        boolean ejecutar = true;
        if (operacion == ConstantesGUI.AGREGAR || operacion == ConstantesGUI.ACTUALIZAR) {
            catalogo.setAula(this.txtAula.getText());
            catalogo.setHora(this.txtHora.getText().trim().trim());
            String periodo = (String) this.JcboxPeriodo.getSelectedItem();

            if (periodo.equals("Opciones")) {
                ejecutar = false;
                this.JcboxPeriodo.setBackground(Color.red);
            }
            catalogo.setPerior(periodo);

            if (this.txtAula.getText().trim() == null) {
                ejecutar = false;
                this.txtAula.setBackground(Color.red);
            }
            if (this.txtHora.getText().trim().trim() == null) {
                ejecutar = false;
                this.txtHora.setBackground(Color.red);
            }

        }

        if (operacion == ConstantesGUI.DESPLEGAR || operacion == ConstantesGUI.ELIMINAR) {
            ejecutar = true;
        }
        if (ejecutar) {
            this.respuesta.delete(0, respuesta.length());
            this.respuesta.append(ConstantesGUI.ACEPTAR);
            this.dispose();
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

